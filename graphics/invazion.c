// Print current score

void showscore ()
{
    sprintf (scoretxt,"%06u",score);
    for (i=0; i<6; i++) {
      putsprite (spr_and, 220+i*5, 2, blank);
      putsprite (spr_or, 220+i*5, 2, &numbers[(scoretxt[i]-48)*7]);
    }
}


 /*************************
         SHOW LIVES
  *************************/
void showlives ()
{
  for (i=0; i<MAXLIVES; i++) {
    if (lives>i)
      putsprite (spr_or, 6+i*6, 2, shp);
    else
      putsprite (spr_and, 6+i*6, 2, shp);
  }
}


